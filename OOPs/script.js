/*
? We use objects to describe real world features
? Objects can contain properties and methods
? Objects are self contained  blocks of code
? Objects are building blocks of an application
? This kind of paradigm was developed to make the code easier to maintain


~ CLASSES
! Classes  are like a blueprint from which we can create objects, it itself is not an object.

~ INSTANCES
! An instance is a real object that we can use in our code, which was created from a class.

~ FOUR FUNDAMENTALS OF OOPS
? ABSTRACTION, ENCAPSULATION, INHERITANCE, POLYMORPHISM

~ ABSTRACTION
! And abstraction basically means to ignore or to hide details that don't matter. This allows us to get an overview perspective of whatever it is that we're implementing instead of messing with details that don't really matter to our implementation.

~ ENCAPSULATION
! The process making the methods of a class private so that they cannot be accessed outside of a class. Some methods can be accessed outside as a public interface.

~ INHERITANCE
! Child class that extends the parent class, inherits some features from the parent class. So inheritance is a process of making all properties and methods of a certain class accessible to the child class.

~ POLYMORPHISM
! This means that a child class can overwrite a method that it inherited from a parent class.
*/